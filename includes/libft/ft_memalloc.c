#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void			*s;
	char			*str;
	unsigned int	i;

	i = 0;
	s = malloc(size);
	str = (char *)s;
	if (s == NULL)
		return (NULL);
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
	str[i] = '\0';
	return (str);
}
