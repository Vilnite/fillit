#include "libft.h"

char	*ft_strnew(size_t size)
{
	char			*str;
	unsigned int	i;
	void			*s;

	i = 0;
	s = malloc(size + 1);
	str = (char *)s;
	if (s == NULL)
		return (NULL);
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
	str[i] = '\0';
	return (str);
}
