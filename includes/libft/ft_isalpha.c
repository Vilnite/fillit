#include "libft.h"

int		ft_isalpha(int x)
{
	int i;

	i = 0;
	if (x >= 'A' && x <= 'Z')
		i = 1;
	else if (x >= 'a' && x <= 'z')
		i = 1;
	else
		i = 0;
	return (i);
}
