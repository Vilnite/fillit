#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	unsigned int	i;
	char			*c1;
	char			*c2;

	i = 0;
	if (n == 0 || s1 == s2)
		return (s1);
	c1 = (char *)s1;
	c2 = (char *)s2;
	while (i < n)
	{
		c1[i] = c2[i];
		i++;
	}
	return (s1);
}
