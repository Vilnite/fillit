#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	unsigned int	i;
	char			*s;

	s = (char *)s2;
	i = 0;
	while (s[i] != '\0')
	{
		s1[i] = s[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
